# 정렬할 리스트를 정의합니다.
arr = [40, 70, 60, 30, 10, 50]

# 리스트의 길이만큼 반복합니다. 매번 가장 큰 값이 끝으로 이동하기 때문에 마지막 요소는 정렬할 필요가 없습니다.
for i in range(len(arr) - 1, 0, -1):
    # 현재 인덱스(i)까지만 반복합니다.
    for j in range(i):
        # 현재 요소(arr[j])와 그 다음 요소(arr[j+1])를 비교합니다.
        if arr[j] > arr[j+1]:
            # 현재 요소가 다음 요소보다 크면 두 요소를 교환합니다.
            arr[j], arr[j+1] = arr[j+1], arr[j]

# 정렬된 리스트를 출력합니다.
print(arr)
